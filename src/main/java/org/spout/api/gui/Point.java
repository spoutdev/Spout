package org.spout.api.gui;

public class Point {

	private int x, y;

	public Point() {
		x = 0;
		y = 0;
	}

	public Point(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public Point(Point other) {
		this.x = other.x;
		this.y = other.y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public Point copy() {
		return new Point(this);
	}

	@Override
	public String toString() {
		return "Point{x=" + x + "; y=" + y + "}";
	}
}
